package com.zs.model;

public class DayStatistics {
    private double fmoney;
    private double fnmoney;
    private double fremoney;
    private double fdeduction;
    private int fstate;
    private int fpaytype;
    private String fctime;

    public double getFmoney() {
        return fmoney;
    }

    public void setFmoney(double fmoney) {
        this.fmoney = fmoney;
    }

    public double getFnmoney() {
        return fnmoney;
    }

    public void setFnmoney(double fnmoney) {
        this.fnmoney = fnmoney;
    }

    public double getFremoney() {
        return fremoney;
    }

    public void setFremoney(double fremoney) {
        this.fremoney = fremoney;
    }

    public double getFdeduction() {
        return fdeduction;
    }

    public void setFdeduction(double fdeduction) {
        this.fdeduction = fdeduction;
    }

    public int getFstate() {
        return fstate;
    }

    public void setFstate(int fstate) {
        this.fstate = fstate;
    }

    public int getFpaytype() {
        return fpaytype;
    }

    public void setFpaytype(int fpaytype) {
        this.fpaytype = fpaytype;
    }

    public String getFctime() {
        return fctime;
    }

    public void setFctime(String fctime) {
        this.fctime = fctime;
    }
}
